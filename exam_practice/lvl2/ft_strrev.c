char	*ft_strrev(char *str)
{
	int i = 0;
	int j; 
	char temp;

	while (str[i])
		i++;
	j = i - 1;
	i = 0;
	while (i < j)
	{
		temp = str[i];
		str[i] = str[j];
		str[j] = temp;
		i++;
		j--;
	}
	return (str);
}
